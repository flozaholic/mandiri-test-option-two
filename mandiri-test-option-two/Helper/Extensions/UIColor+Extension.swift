import Foundation
import UIKit

extension UIColor {
    
    class var primary: UIColor {
        return #colorLiteral(red: 0.6980392157, green: 0.8470588235, blue: 0.8470588235, alpha: 1)
    }
    
    class var accent: UIColor {
        return #colorLiteral(red: 0, green: 0.5019607843, blue: 0.5019607843, alpha: 1)
    }
    
    class var greenText: UIColor {
        return #colorLiteral(red: 0, green: 0.2980392157, blue: 0.2980392157, alpha: 1)
    }
    
}
